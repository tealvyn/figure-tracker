export function t(key, vars = {}) {
  const lang = getLang();
  let value = translations[lang]?.[key] ?? translations.ru[key] ?? key;
  for (const [name, replacement] of Object.entries(vars)) {
    value = value.replaceAll(`{${name}}`, String(replacement));
  }
  return value;
}

export function setLang(lang) {
  const next = lang === 'en' ? 'en' : 'ru';
  localStorage.setItem(LANG_KEY, next);
  document.documentElement.lang = next;
  applyI18n();
  window.dispatchEvent(new CustomEvent('fct:language-change', { detail: { lang: next } }));
}

export function applyI18n(root = document) {
  const lang = getLang();
  document.documentElement.lang = lang;
  root.querySelectorAll('[data-i18n]').forEach(el => { el.textContent = t(el.dataset.i18n); });
  root.querySelectorAll('[data-i18n-placeholder]').forEach(el => { el.placeholder = t(el.dataset.i18nPlaceholder); });
  root.querySelectorAll('[data-i18n-title]').forEach(el => { el.title = t(el.dataset.i18nTitle); });
  root.querySelectorAll('[data-i18n-tip]').forEach(el => { el.dataset.tip = t(el.dataset.i18nTip); });
  root.querySelectorAll('[data-i18n-value]').forEach(el => { el.value = t(el.dataset.i18nValue); });
  root.querySelectorAll('[data-lang-option]').forEach(el => {
    el.classList.toggle('active', el.dataset.langOption === lang);
    el.setAttribute('aria-pressed', String(el.dataset.langOption === lang));
  });
}

export function initLanguageControls() {
  document.addEventListener('click', event => {
    const button = event.target.closest('[data-lang-option]');
    if (!button) return;
    setLang(button.dataset.langOption);
  });
  applyI18n();
}



